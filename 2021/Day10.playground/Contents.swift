struct CorruptedLine: Error {
    let points: Int
}

func unclosedCharacters(line: String) throws -> [Character] {
    var state: [Character] = []
    
    try line
        .map { char in
            switch char {
            case "(", "[", "{", "<": state.append(char)

            case ")": if (state.popLast() != "(") { throw CorruptedLine(points: 3) }
            case "]": if (state.popLast() != "[") { throw CorruptedLine(points: 57) }
            case "}": if (state.popLast() != "{") { throw CorruptedLine(points: 1197) }
            case ">": if (state.popLast() != "<") { throw CorruptedLine(points: 25137) }

            default: fatalError()
            }
        }

    return state;
}

